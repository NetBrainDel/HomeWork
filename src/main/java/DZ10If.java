public class DZ10If {
    public static void main(String[] args) {
        int a = 1;
        int b = 2;

        if (a > b) {
            System.out.println(a + " наибольшее число под буквой 'a'");
            System.out.println(b + " меньшее число под буквой 'a'");
        } else {
            System.out.println(b + " наибольшее число под буквой 'b'");
            System.out.println(a + " меньшее число под буквой 'a'");
        }
    }
}

